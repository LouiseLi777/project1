package edu.duke.ece65.team2.AttendanceManager;

/**
 * Enum for the status of a student's attendance.
 */
public enum AttendanceStatus {
    /**
     * The student is present.
     */
    PRESENT,
    /**
     * The student is absent.
     */
    ABSENT,
    /**
     * The student is tardy.
     */
    TARDY
}

